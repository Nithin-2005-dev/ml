import numpy as np
import pandas as pd
from sklearn.model_selection import train_test_split
from sklearn.linear_model import LogisticRegression
from sklearn.metrics import accuracy_score

dataset=pd.read_csv('/content/heart_disease_data.csv')

dataset.head()

dataset.info()

dataset.isnull().sum()

dataset.describe()

dataset.value_counts('target')

data=dataset.drop(columns='target',axis=1)
label=dataset['target']

print(data,label)

data_train,data_test,label_train,label_test=train_test_split(data,label,test_size=0.2,stratify=label,random_state=2)

model=LogisticRegression()

model.fit(data_train,label_train)

data_train_prediction=model.predict(data_train)
training_prediction=accuracy_score(data_train_prediction,label_train)
print(training_prediction)

data_test_prediction=model.predict(data_test)
testing_prediction=accuracy_score(data_test_prediction,label_test)
print(testing_prediction)

input_data=(58,1,2,140,211,1,0,165,0,0,2,0,2)
np_array=np.asarray(input_data)
np_array_reshaped=np_array.reshape(1,-1)
prediction=model.predict(np_array_reshaped)
if(prediction[0]==1):
  print('heart disease detected')
else:
  print('no heart disease')

